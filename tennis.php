        <b>History</b> </br>
        <p>Tennis balls of the past were made of a variety of different materials. Many types of animal furs and innards, tied up with string have been used in years gone by. King Louis XI of France also insisted tennis balls in France should be made of leather, stuffed with wool. As a result of all these variations over the years, tennis evolved at different rates around the world. Vulcanised rubber was first used for tennis balls in 1870 with the introduction of lawn tennis. Tennis balls were first sold in cylindrical tubes in 1925, when the Wilson-Western Sports Company came up with the idea.
            Nowadays, upwards of 300 millions balls are produced each year!</p></br>
            <b>Standard Modern Balls</b> </br>
        <p>These days balls must meet certain standard requirements. Obviously for fair international competitions, everyone must use similar balls around the world. The standardisation of balls is controlled by the ITF.
            Standard modern balls should have a diameter of between 2.575 inches and 2.7 inches. They should also weigh no less than 56 grams, and no more than 59.4 grams. The only colours approved for competitive play are yellow and white. Since 1972 the standard ball has been 'flourescent yellow', after a study showed that this was better for television coverage.
              Tennis balls are filled with air, which is then surfaced by a uniform felt-covered rubber compound. They are generally then kept in a pressurised container until they are required for play. As soon as a tin of balls is open and they are removed from these pressurised conditions a ball will begin to lose its bounce. At the top level balls are generally kept in these conditions until the last possible point. The loss in bounce is also why the balls are changed so regularly at elite level.
          </p></br>
          <b>Other Types Of Balls</b> </br>
        <p>In some situations you may be better to use different sorts of balls. The obvious example of this is with beginners or very young players. Other balls are made which may be slower and easier for inexperienced performers to learn with. This is one of the key features of the ITF's Play & Stay campaign. This follows a red, orange and green progression - with red balls being the slowest and easiest for beginners. As players learn skills they can progress along the pathway towards using the standard balls.</p></br>
