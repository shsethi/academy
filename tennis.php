        <p>The standardisation of the rules of tennis is the responsibility of the ITF. Broadly speaking it is a very simple game to get to grips with. Here is a very basic beginners guide to the rules to help you get started:</p></br>
        <b>The Basics </b></br>
        <p>  Tennis is played either 1v1 (singles) or 2v2 (doubles). The teams will be on opposite sides of the net, and will use rackets to hit a ball back and forth across the net. Each team has one bounce to return the ball back over the net and into the opponents court. Failure to do so means the opponent scores a point. Points can be turned into games, and games into sets, with matches being decided by whichever team has won the required number of sets.
			Men's matches can be played up to a maximum of best-of-5 sets, and women's to a maximum of best-of-3.</p>
		<b> Scoring</b></br>
		<p> Tennis is renowned for its slightly odd scoring system. Points don't go up in the usual way, 1, 2, 3 etc. Instead points go up as 15, 30, 40 in tennis. For example, if both players have scored 2 points in a game, then the score would be 30-30.
			The first player to score 4 points wins that particular game. The only exception to this rule being when both players/teams have won 3 points each (40-40). This score is known as 'deuce', and it becomes the first to win 2 points in a row that wins the game.
			Usually the first team/player to win 6 games wins that set. However a margin of 2 games is required for victory. If the score reaches 5 games all then a set can be won 7-5. Traditionally a 2 game margin was always required and thus sets could last infinitely. However in more recent times a 'tie-break' has been adopted to decide close sets once they reach 6 games all. This move was made to limit match lengths and make matches more exciting.</p>      
			These 'tie-breaks' are played as the first to win 7 points - although once again a 2 point margin is required for victory. The winner of the 'tie-break' is the winner of the set.
			The majority of the Grand Slam's do not use 'tie-breaks' to decide a 5th set however, and still require a 2 game margin for victory. This means matches can last for a very lengthy time. This was demonstrated at Wimbledon 2010 when John Isner defeated Nicolas Mahut by 70 games to 68 in the 5th set of their first round encounter. The match lasted for over 11 hours - a record which is unlikely to be eclipsed.</br>
      	<b>Serving</b></br>
      	<p>Players must serve cross-court from behind their baseline, into the service box of their opponent. Players have 2 chances to successfully do this, or they lose the point.</p>
