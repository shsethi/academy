      <div class="panel-body">
        <p>Injuries of the shoulder, elbow and wrist are amongst the most common injuries seen in tennis players. However, due to the bursts of speed and change of direction nature of the game, ankle and knee injuries are also common. </p></br>
        <b>Tennis elbow</b> </br>
        <p>This is the injury that everybody associates with tennis. However it is more commonly seen in people who have never played tennis, than those that do regularly. It is an overuse injury of the wrist extensor muscles, where they attach to the lateral epicondyle (bony part on the outside of the elbow). Symptoms include pain on wrist extension (bending the wrist back) and gripping the racket. Causes include poor technique and wrong grip size. </p></br>
        <b>Carpal tunnel syndrome </b> </br>
        <p>Carpal tunnel syndrome is where the median nerve is compressed within the wrist. In tennis, if there is no previous injury to the wrist, the cause is likely overuse and inflammatory causing swelling within the wrist and narrowing the carpal tunnel. Sometimes the cause can be structural due to a naturally smaller carpal tunnel. Carpal tunnel causes pain in the wrist and handed, often accompanied by tingling in the fingers.</p></br>
        <b>Shoulder impingement</b> </br>
    <p>
    This refers to trapping and pinching of one or more of the rotator cuff tendons. This occurs due to narrowing of the space through which these tendons pass. The most common cause of this reduced space is biomechanical abnormality, usually associated with poor posture. Symptoms in shoulder impingement include pain with overhead movements - especially serving. Treatment usually consists of rest and ice, followed by a rehabilitation program. </br>
    </p>
    <b>Back injuries </b><br>
      <p>Back injuries are reported to make up to 16% of all tennis injuries. The most stress is placed on the spine during the serve, in both the extension phase when tossing the ball and in the action of the serve itself. Both movements involve a rotation force in conjunction with the main motion of flexion or extension. Facet joint injuries are one of the more common back injuries seen in tennis. </p></br>
        <b>Ankle sprains</b> </br>
    <p>Lateral ankle sprains are the most common ankle injury both in and out of sport and tennis is no exception. The fast paced changes of direction can lead to the ankle rolling over and damaging one of more of the lateral ligaments. This type of injury can be prevented by strengthening the calf, shin and peroneal muscles, ensuring good flexibility and performing proprioception exercises (using wobble boards etc).</p> </br>
      <b>Patella tendinopathy</b><br>
      <p>Also known as jumpers knee, patella tendinopathy is caused by repetitive strain on the tendon which connects the knee cap and quadriceps to the shin bone. Repetitive strain causes degenerative changes to the tendon, resulting in pain on jumping and running activities. Treatment should involve looking at the biomechanics of the player and correcting any problems such as a weakness in the VMO muscle or tightness in the quadriceps, as well as strengthening the quads eccentrically (with gravity so the muscle lengthens under tension). </p></br>

