        <p>Tennis, being a completely non-contact sport, has a lower incidence of injury, or at least acute, traumatic injury, that sports such as football. The majority of tennis injuries are more chronic overuse injuries such as tendonitis.</p></br>
        <b>Prevention</b> </br>
        <p>A large number of injuries, in all sports, especially at the lower levels, could be avoided. The following should not be neglected:</p></br>
        <b>Warm-up</b> </br>
        <p>The warm-up is one of the most important factors in all sports, in order to stay injury free. Tennis is a whole body sport and for that reason, the whole body should be warm-up and stretched, prior to hitting that first ball! Cardiovascular exercise should be the first component of a warm-up routine, in order to increase the circulation and warm the muscles. This should be followed by active stretches such as dynamic lunges and shoulder and wrist circles. Static stretches can also be performed.</p></br>
        <b>Equipment</b> </br>
    <p>
    The racket is the most obvious piece of equipment that a tennis player must ensure is right for them, to help avoid injuries. The most important factor is grip size. A grip size that is too small often places additional strain on the wrist, whereas a grip which is too large requires extra contraction of the forearm muscles and can contribute towards developing tennis elbow. A light racket and looser strings are less likely to cause injury.
    The availability and range of specific tennis footwear has improved in recent years. Shoes now have thicker rubber soles, reinforced mid-foot support and a wider toe area, all features designed to enable optimum performance and limit injury.    </p></br>
    <b>Technique</b><br>
    In order to avoid injury, the correct technique should be taught from the start. Learning the right techniques will help to reduce overuse injuries, such as tennis elbow, which is sometimes caused by a poor backhand stroke.
        <b>Strength</b> </br>
    <p>    
    Strength is important with any sport, but must be specific to the sport in question. Tennis is a fast paced sport requiring changes of direction and sudden bursts of speed. This requires a strong core to maintain the bodies centre of balance - important in avoiding injuries. Strength of the shoulder girdle, including the rotator cuff muscles, is also important in injury prevention.</p></br>
