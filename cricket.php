        <p>

          The most common types of injuries suffered through cricket are strains, sprains, fractures, bruising and open wounds. Many of these injuries affect the upper body, particularly the hands and fingers. 

          Injuries to the face, finger and hand from a cricket ball are the most common types of hospital-treated injuries, followed by falls. Bowlers are inclined to suffer overuse injuries, such as back and shoulder injuries</p></br>
        
        
        <p><b>Impact - </b> </br>e.g. being hit by a ball or collision with a player or the ground. Some of these are unavoidable although better judgement, footspeed, and protective clothing help reduce the incidence.</br>
              <b>indirect - </b> </br>e.g. muscle, ligament and tendon damage sustained while attempting to perform a specific activity. These injuries can often be prevented as they sometimes occur because of a poor warm-up routine or a lack of cricket fitness.</br>
                <b>overuse - </b> </br>e.g. repetitive movements that create, for example, a muscle imbalance or excessive stress on a particular area e.g. shoulder for bowlers. The most common recipients of overuse injuries are fast bowlers due to the repetitive extension, twisting and rotation of the trunk during the bowling movement.</p></br>
