        <p>Soccer is thought to be a relatively safe sport. This is in comparison to sports such as American football and ice hockey. A high-school football player is at 85% risk of injury, where a soccer player is only 30% at risk. Within soccer, players over 25 and female players are more at risk of injury than their younger male counterparts.

Injuries to the lower limb are most common, especially to the ankle and knee. The most common moderate to severe lower limb injuries are listed here. Other minor injuries include contusions, minor muscle strains and abrasions.</p></br>
        <b>Ankle sprains </b> </br>
        <p>Ankle sprains are the most common injury amongst all levels of soccer player and account for a massive 36% of all injuries. This injury is most commonly sustained when running and changing direction quickly, or when tackling or being tackled. An inversion (lateral) sprain of the ankle occurs when the ankle is rolled over so that the sole of the foot faces inwards. Eversion (medial) sprains are far less common.</p></br>
        <b>ACL tears</b> </br>
    <p>
    The ACL is the anterior cruciate ligament which is one of the most frequently damaged ligaments in the game of soccer. It is situated deep within the knee joint and has the job of maintaining the position of the Tibia (shin bone) underneath the Femur (thigh bone). The majority of these injuries occur in a non-contact situation at a point where the player lands or decelerates with a twisting motion. Injury to the ACL can also occur following side on contact such as a slide tackle from an opponent.</p>
    <b>Hamstring strains </b><br>
      <p>The hamstrings are the most commonly torn muscles in soccer, followed closely by the adductor (groin) muscles. A tear to one of the hamstring muscles most often occurs during a burst of speed especially in muscles which are either fatigued or have been inadequately warmed-up.</p></br>
        <b>Meniscus Injuries</b> </br>
    <p>The meniscus are two rings of cartilage which sit on top of the Tibia (shin bone), between this and the Femur (thigh bone). A meniscus tear is not uncommon in soccer and usually occurs in a tackle in which there is a twisting motion, usually with the foot fixed. Degeneration of the cartilage can also become a problem in older players, especially following previous knee injuries.</p> </br>
